    @commands.command(aliases=['img2ascii', 'asciify'])
    async def image2ascii(
        self, 
        ctx, 
        size: int = 50, # 50 is reasonable small, can always change if ya wish
        *, chars: str = 'braille' # character set
        ) -> None:
        """
        Converts an image to ASCII art
        """

        """
        await image2ascii(context, size, characters) -> nothing

        Turns the given image into ASCII art

        :param ctx object: Context
        :param size int: Size of the ASCII art
        :param chars str: Character set to use when picking characters for the pixels
        :returns None: Nothing
        """

        if chars == 'braille':
            chars = '⠁⠂⠃⠄⠅⠆⠇⠈⠉⠊⠋⠌⠍⠎⠏⠐⠑⠒⠓⠔⠕⠖⠗⠘⠙⠚⠛⠜⠝⠞⠟⠠⠡⠢⠣⠤⠥⠦⠧⠨⠩⠪⠫⠬⠭⠮⠯⠰⠱⠲⠳⠴⠵⠶⠷⠸⠹⠺⠻⠼⠽⠾⠿'

        elif chars == 'basic':
            chars = 'MNHQ$OC?7>!:-;. '

        url = None
        if len(ctx.message.attachments) != 0:
            attachment = ctx.message.attachments[0]

            # allowed file extensions
            if attachment.filename.endswith(".jpg") \
            or attachment.filename.endswith(".jpeg") \
            or attachment.filename.endswith(".png"):
                url = attachment.url
            
            else:
                await sendmsg(ctx, '**Error**: `Image to ascii art converter only accepts JPG, JPEG and PNG`')
                return
            
        else:
            await sendmsg(ctx, '**Error**: `Please add an image`')
            return

        if not url:
            await sendmsg(ctx, '**Error**: `Could not get image url`')
            return

        async with aiohttp.ClientSession() as cs:
            async with cs.get(url) as res:
                resp = await res.read()

        image = Image.open(BytesIO(resp))
        width, height = image.size

        ratio = float(size) / max(width, height)
        width = int(ratio * width)
        height = int(ratio * height)

        image = image.resize((width, height))

        image = image.convert("L")  # converting image to grayscale
        pixels = image.getdata()  # getting data of pixels in the grayscaled image
        characters = "".join(
            [chars[pixel//25] for pixel in pixels]
        )

        pixel_count = len(characters)
        ascii_image = "\n".join(
            [characters[index: index + size]
                for index in range(0, pixel_count, size)]
        )

        if len(ascii_image) > 2000:
            await sendmsg(
                ctx,
                '**Error**: `ASCII art is too big! Please try a smaller image or change the size`'
            )

        else:
            await asyncio.sleep(uniform(1, 2)) 
            await ctx.message.edit(
                content=ascii_image,
                attachments=[]
            ) # remove attachments aswell
    
